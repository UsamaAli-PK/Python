def display_data_types():
    # Integer
    integer_example = 42
    print(f"Integer: {integer_example}")

    # Float
    float_example = 3.14159
    print(f"Float: {float_example}")

    # Complex
    complex_example = 2 + 3j
    print(f"Complex: {complex_example}")

    # String
    string_example = "Hello, world!"
    print(f"String: '{string_example}'")

    # List
    list_example = [1, 2, 3, 4, 5]
    print(f"List: {list_example}")

    # Tuple
    tuple_example = (1, 2, 3, 4, 5)
    print(f"Tuple: {tuple_example}")

    # Set
    set_example = {1, 2, 3, 4, 5}
    print(f"Set: {set_example}")

    # Dictionary
    dict_example = {'name': 'Alice', 'age': 30}
    print(f"Dictionary: {dict_example}")

    # Boolean
    boolean_true = True
    boolean_false = False
    print(f"Boolean True: {boolean_true}")
    print(f"Boolean False: {boolean_false}")

    # NoneType
    none_example = None
    print(f"NoneType: {none_example}")

# Call the function to display data types
display_data_types()




